/*Conditionals*/
// two equals == transform the value to the same type and compare
if (1 == '1'){
    console.log('\n--- if == ---');
    console.log('2 equals');
}

// three equals === don't change the type, that means that if you have the same but in different type is false
if (1 === '1'){
    console.log('\n--- if === ---');
    console.log('3 equals');
}

//if else
let flag = true;
if (flag){
    console.log(true);
}else{
    console.log(false)
}
